SELECT DISTINCT EXPORT_COUNTRY 
FROM CUSTOMERS;

SELECT CUSTOMER_NAME, PRODUCT_NAME, ORDER_VALUE
FROM CUSTOMERS
WHERE EXPORT_COUNTRY IN ('France', 'USA')
ORDER BY ORDER_VALUE DESC;

SELECT EXPORT_COUNTRY, SUM(ORDER_VALUE) AS Total_Sales
FROM CUSTOMERS
GROUP BY EXPORT_COUNTRY
ORDER BY Total_Sales DESC;