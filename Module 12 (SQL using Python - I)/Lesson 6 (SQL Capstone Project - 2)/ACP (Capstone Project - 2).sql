CREATE TABLE CUSTOMERS (
    CUSTOMER_ID INTEGER PRIMARY KEY,
    CUSTOMER_NAME TEXT NOT NULL,
    PRODUCT_NAME TEXT,
    EXPORT_COUNTRY TEXT,
    ORDER_VALUE REAL
