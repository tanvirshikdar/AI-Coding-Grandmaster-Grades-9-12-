DROP TABLE IF EXISTS EMPLOYEES;

CREATE TABLE EMPLOYEES (
    EMP_ID INTEGER PRIMARY KEY,
    NAME TEXT NOT NULL,
    ROLE TEXT,
    DEPARTMENT TEXT,
    SALARY REAL,
    JOIN_DATE TEXT
);

