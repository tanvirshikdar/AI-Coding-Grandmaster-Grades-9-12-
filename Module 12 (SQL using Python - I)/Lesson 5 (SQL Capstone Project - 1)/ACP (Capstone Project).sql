SELECT 
    SUM(SALARY) AS Company_Total_Payroll,
    AVG(SALARY) AS Average_Salary
FROM EMPLOYEES;

SELECT DEPARTMENT, COUNT(*) AS Employee_Count
FROM EMPLOYEES
GROUP BY DEPARTMENT;

SELECT DEPARTMENT, AVG(SALARY) AS Dept_Avg
FROM EMPLOYEES
GROUP BY DEPARTMENT
HAVING Dept_Avg > 80000;

SELECT NAME, ROLE, SALARY
FROM EMPLOYEES
ORDER BY SALARY DESC;

